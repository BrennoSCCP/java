
package Capitulo03;


public class Pessoa {
    //atributos
    private String nome;
    private int idade;
    
    //metodos
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    public String getNome() {
        return nome;
    }
    
    public int getIdade() {
        return idade;
    }
}

 


